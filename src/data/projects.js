/**
 * Get project by slug
 * @param {string} slug - The project slug
 * @returns {object|undefined} - The project object or undefined
 */
export function getProjectBySlug(slug) {
  return projects.find((project) => project.slug === slug);
}

/**
 * Get project index by slug
 * @param {string} slug - The project slug
 * @returns {number} - The project index or -1 if not found
 */
export function getProjectIndexBySlug(slug) {
  return projects.findIndex((project) => project.slug === slug);
}
